class Operator {
    public static void main(String[] args) {
        // dividing two integers will give you an integer result
        int num1 = 7;
        int num2 = 5;
        int result = num1 / num2;
        System.out.println("Result: " + result);

        // incrementing a number
        num1 += 2;
        num1++; // post-increment
        ++num1; // pre-increment
        num1--;
        System.out.println("num1: " + num1);
    }
}
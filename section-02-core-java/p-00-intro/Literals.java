class Literals {
    
    public static void main(String a[]) {
        // can add underscores to values for easy readability
        double d = 1_000_000.00;
        System.out.println("DOUBLE: " + d);
        // can use e for scientific notation
        double num1 = 12e10;
        System.out.println("BIG NUMBER: " + num1);
        char c = 'A';
        c++;
        System.out.println("CHAR: " + c);
    }
}